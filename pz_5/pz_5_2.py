# Описать функцию AddLeftDigit(D, K), добавляющую к целому положительному
# числу K слева цифру D (D — входной параметр целого типа, лежащий в диапазоне
# 1-9, K — параметр целого типа, являющийся одновременно входным и выходным).
# С помощью этой функции последовательно добавить к данному числу K слева
# данные цифры D1 и D2, выводя результат каждого добавления.

def AddLeftDigit(D, K):
    K = int(str(D) + str(K))
    return K

# Ввод числа K
K = input("Введите целое положительное число K: ")
while True:  # обработка исключений
    try:
        K = int(K)
        if K > 0:  # Проверка на положительность
            break
        else:
            print("Ошибка: Число K должно быть положительным.")
            K = input("Введите целое положительное число K: ")
    except ValueError:
        print("Неправильно ввели! Попробуйте снова.")
        K = input("Введите целое положительное число K: ")

# Ввод D1
D1 = input("Введите первую цифру D1 (от 1 до 9): ")
while True:  # обработка исключений
    try:
        D1 = int(D1)
        if 1 <= D1 <= 9:  # Проверка на диапазон
            break
        else:
            print("Ошибка: D1 должно быть от 1 до 9.")
            D1 = input("Введите первую цифру D1 (от 1 до 9): ")
    except ValueError:
        print("Неправильно ввели! Попробуйте снова.")
        D1 = input("Введите первую цифру D1 (от 1 до 9): ")

# Добавляем D1 слева к K и выводим результат
K = AddLeftDigit(D1, K)
print("Число после добавления D1: ", K)

# Ввод D2
D2 = input("Введите вторую цифру D2 (от 1 до 9): ")
while True:
    try:
        D2 = int(D2)
        if 1 <= D2 <= 9:  # Проверка на диапазон
            break
        else:
            print("Ошибка: D2 должно быть от 1 до 9.")
            D2 = input("Введите вторую цифру D2 (от 1 до 9): ")
    except ValueError:
        print("Неправильно ввели! Попробуйте снова.")
        D2 = input("Введите вторую цифру D2 (от 1 до 9): ")

# Добавляем D2 слева к K и выводим результат
K = AddLeftDigit(D2, K)
print("Число после добавления D2: ", K)